<?php
echo "<h2>Задание 6: Пользовательская функция (кусочная)</h2>";

function calculatePiecewise($a, $x) {
    // Проверка на допустимость входных данных
    if (!is_numeric($a) || !is_numeric($x)) {
        throw new Exception("Ошибка: a и x должны быть числами.");
    }

    // Первая ветка: x ≥ 2
    if ($x >= 2) {
        // Проверка деления на ноль в знаменателе x(1 - 1.5)
        $denom = $x * (1 - 1.5); // 1 - 1.5 = -0.5
        if ($denom == 0) {
            throw new Exception("Ошибка: деление на ноль в первой ветке при x = $x");
        }

        // Проверка подкоренного выражения a * x
        if ($a * $x < 0) {
            throw new Exception("Ошибка: подкоренное выражение отрицательное (a * x < 0) при a = $a, x = $x");
        }

        return ($a / $denom) + sqrt($a * $x);
    }

    // Вторая ветка: 0 ≤ x < 2
    if ($x >= 0 && $x < 2) {
        // Проверка подкоренного выражения для sqrt(a)
        if ($a < 0) {
            throw new Exception("Ошибка: подкоренное выражение отрицательное (a < 0) при a = $a");
        }

        return 2 * sqrt($a) - ($x / 4);
    }

    // Третья ветка: x < 3 (но, как объяснено выше, сработает только если x < 0)
    if ($x < 3) {
        return 0.3 * $x;
    }

    // Если ни одно условие не подошло (теоретически不可能)
    throw new Exception("Неизвестная ошибка: ни одно условие не выполнено");
}

// Тестовые примеры
$test_cases = [
    ['a' => 4, 'x' => 5],   // x ≥ 2
    ['a' => 4, 'x' => 1],   // 0 ≤ x < 2
    ['a' => 4, 'x' => -2],  // x < 0 → третья ветка
    ['a' => -4, 'x' => 1],  // ошибка: a < 0 во второй ветке
    ['a' => 4, 'x' => 0],   // вторая ветка, x = 0
    ['a' => 4, 'x' => 2],   // первая ветка (ровно 2)
    ['a' => 0, 'x' => 5],   // первая ветка, a=0
];

foreach ($test_cases as $case) {
    $a = $case['a'];
    $x = $case['x'];
    try {
        $result = calculatePiecewise($a, $x);
        echo "При a = $a, x = $x → результат y = $result<br>";
    } catch (Exception $e) {
        echo "При a = $a, x = $x: " . $e->getMessage() . "<br>";
    }
}
